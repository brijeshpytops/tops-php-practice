<!-- 
Increment/Decrement Operators: Used to increment or decrement the value of a variable. Example: ++, --.
 -->

 <?php
$num1 = 10;

// Increment operator (++)
$num1++; // Equivalent to $num1 = $num1 + 1; or $num1 += 1;
echo $num1 . "<br>"; // Outputs: 11

// Decrement operator (--)
$num1--; // Equivalent to $num1 = $num1 - 1; or $num1 -= 1;
echo $num1 . "<br>"; // Outputs: 10 (back to the original value)

?>