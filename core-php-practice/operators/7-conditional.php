<!-- 
Conditional (Ternary) Operator: Used to assign a value to a variable based on a condition. 
Example: condition ? value_if_true : value_if_false.
 -->

<?php
// Define a variable
$age = 20;

// Ternary operator usage
$is_adult = ($age >= 18) ? "Yes" : "No";

// Output the result
echo "Is the person an adult? " . $is_adult;

?>